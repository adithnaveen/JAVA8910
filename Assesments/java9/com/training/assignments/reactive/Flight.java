package com.training.assignments.reactive; 

public class Flight {
	private String name;
	private double seatingCapacity;

	
	
	public Flight(String name, double seatingCapacity) {
		super();
		this.name = name;
		this.seatingCapacity = seatingCapacity;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Flight [name=" + name + ", seatingCapacity=" + seatingCapacity + "]";
	}

	public double getSeatingCapacity() {
		return seatingCapacity;
	}

	public void setSeatingCapacity(double seatingCapacity) {
		this.seatingCapacity = seatingCapacity;
	}

}
